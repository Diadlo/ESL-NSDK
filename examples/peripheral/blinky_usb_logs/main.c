/**
 * @brief Function for application main entry.
 */
int main(void)
{
    /* Configure board. */
    bsp_board_init(BSP_INIT_LEDS);

    usbd_init();

    log_init();

    usbd_start();

    NRF_LOG_INFO("Start");

    /* Toggle LEDs. */
    while (true)
    {
        //NRF_LOG_INFO("Iter");
        NRF_LOG_PROCESS();
        while (app_usbd_event_queue_process())
        {
            /* Nothing to do */
        }

    }
}

